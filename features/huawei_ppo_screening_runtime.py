def _dedupe(values: Sequence[str]) -> tuple[str, ...]:
    return tuple(dict.fromkeys(str(value) for value in values))


def _candidate_ancestors(start: Path, limit: int = 8) -> list[Path]:
    current = start.resolve()
    out = [current]
    for _ in range(limit):
        parent = current.parent
        if parent == current:
            break
        out.append(parent)
        current = parent
    return out


def _first_existing(candidates: Sequence[Path], *, expected_file: str | None = None) -> Path | None:
    for candidate in candidates:
        path = candidate.expanduser().resolve()
        if expected_file:
            if (path / expected_file).exists():
                return path
        elif path.exists():
            return path
    return None


def resolve_package_dir(project_root: str | Path | None = None, package_dir: str | Path | None = None) -> Path:
    if package_dir:
        resolved = Path(package_dir).expanduser().resolve()
        if not (resolved / MANIFEST_NAME).exists():
            raise FileNotFoundError(f"Manifest not found in explicit package_dir: {resolved}")
        return resolved

    root = Path(project_root or Path.cwd()).expanduser().resolve()
    candidates: list[Path] = []
    env_value = os.environ.get("HUAWEI_PPO_SCREENING_PACKAGE_DIR")
    if env_value:
        candidates.append(Path(env_value))
    for ancestor in _candidate_ancestors(root):
        candidates.extend(
            [
                ancestor,
                ancestor / "huawei_screening_seed42",
                ancestor / "ppo_ablation_package" / "huawei_screening_seed42",
                ancestor / DEFAULT_PACKAGE_RELATIVE_PATH,
            ]
        )
    found = _first_existing(candidates, expected_file=MANIFEST_NAME)
    if found is None:
        raise FileNotFoundError(
            "Could not locate huawei_ppo_screening_manifest.json. "
            "Set HUAWEI_PPO_SCREENING_PACKAGE_DIR or pass package_dir explicitly."
        )
    return found


def load_manifest(package_dir: str | Path) -> dict[str, Any]:
    path = Path(package_dir) / MANIFEST_NAME
    return json.loads(path.read_text(encoding="utf-8"))


def get_experiment(manifest: Mapping[str, Any], variant_name: str) -> dict[str, Any]:
    for experiment in manifest.get("experiments", []):
        if str(experiment.get("name")) == str(variant_name):
            return dict(experiment)
    known = [str(item.get("name")) for item in manifest.get("experiments", [])]
    raise KeyError(f"Unknown experiment variant {variant_name!r}. Known variants: {known}")


